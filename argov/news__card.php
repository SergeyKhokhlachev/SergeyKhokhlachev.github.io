                Президент РФ Владимир Путин подписал федеральный закон № 206-ФЗ «О внесении изменений в статьи 19.6.1 и 28.3 Кодекса Российской Федерации об административных правонарушениях», подготовленный Минэкономразвития России. Документ предусматривает вынесение должностным лицам органов, осуществляющим контрольно-надзорную деятельность, предупреждения или наложение на них штрафа в размере от 1000 до 3000 рублей за нарушения, выразившиеся в невнесении информации о проверке в единый реестр проверок (ЕРП), нарушении два и более раза в течение одного года сроков внесения сведений в ЕРП,  внесении два и более раз в течение одного года неполной или недостоверной информации о проверке в ЕРП.
