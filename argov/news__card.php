              <p>
                Единый реестр — это федеральная государственная информационная система, оператором которой является Генеральная прокуратура РФ, нормативное обеспечение — осуществляет Минэкономразвития. В соответствии с действующим законодательством органы государственного контроля (надзора) и органы муниципального контроля обязаны вносить туда данные о начатых, проводимых и законченных проверках и их результатах.
              </p>
